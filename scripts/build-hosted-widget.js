const fs = require('fs');
const path = require('path');

const rootDir = path.resolve(__dirname, '..');
const sourcePath = path.join(rootDir, 'reviews-widget.html');
const outDir = path.join(rootDir, 'server', 'public', 'widgets');
const loaderPath = path.join(outDir, 'reviews.js');
const bundlePath = path.join(outDir, 'reviews.bundle.js');

const source = fs.readFileSync(sourcePath, 'utf8');
const scriptOpen = source.lastIndexOf('<script>');
const scriptClose = source.lastIndexOf('</script>');

if (scriptOpen < 0 || scriptClose < scriptOpen) {
  throw new Error('Main widget <script> block was not found at the end of reviews-widget.html');
}

const scriptCode = source.slice(scriptOpen + '<script>'.length, scriptClose).trim();
const html = source.slice(0, scriptOpen).trim();

if (!html.includes('id="yc-widget"') || !scriptCode.includes('(function(){')) {
  throw new Error('Widget source split failed: expected HTML widget and main IIFE script');
}

const bundled = `// Generated by scripts/build-hosted-widget.js from reviews-widget.html
// Do not edit this file directly; edit reviews-widget.html and rebuild.
(function() {
  'use strict';

  var loaderScript = document.currentScript;
  var widgetHtml = ${JSON.stringify(html)};
  var widgetCode = ${JSON.stringify(scriptCode)};

  function findRoot() {
    var configuredTarget = loaderScript && loaderScript.getAttribute('data-target');
    if (configuredTarget) {
      var configuredRoot = document.querySelector(configuredTarget);
      if (configuredRoot) return configuredRoot;
    }

    if (loaderScript && loaderScript.parentElement && loaderScript.parentElement.hasAttribute('data-mbs-reviews-widget')) {
      return loaderScript.parentElement;
    }

    return document.querySelector('[data-mbs-reviews-widget]') ||
      document.getElementById('mbs-reviews-widget') ||
      (loaderScript && loaderScript.parentElement) ||
      null;
  }

  function mount() {
    var root = findRoot();
    if (!root || root.getAttribute('data-mbs-reviews-loaded') === '1') return;
    root.setAttribute('data-mbs-reviews-loaded', '1');

    var existingWidget = document.getElementById('yc-widget');
    if (existingWidget && !root.contains(existingWidget)) {
      existingWidget.parentElement.removeChild(existingWidget);
    }

    root.innerHTML = widgetHtml;

    var inlineScript = document.createElement('script');
    inlineScript.text = widgetCode + '\\n//# sourceURL=https://api.barista-school.ru/widgets/reviews.inline.js';
    root.appendChild(inlineScript);
  }

  if (document.readyState === 'loading') {
    document.addEventListener('DOMContentLoaded', mount, { once: true });
  } else {
    mount();
  }
})();
`;

const loader = `// Generated by scripts/build-hosted-widget.js.
// Tiny non-blocking loader for Tilda. The heavy widget lives in reviews.bundle.js.
(function() {
  'use strict';

  var loaderScript = document.currentScript;
  var root = null;

  function findRoot() {
    var configuredTarget = loaderScript && loaderScript.getAttribute('data-target');
    if (configuredTarget) {
      var configuredRoot = document.querySelector(configuredTarget);
      if (configuredRoot) return configuredRoot;
    }

    if (loaderScript && loaderScript.previousElementSibling && loaderScript.previousElementSibling.hasAttribute('data-mbs-reviews-widget')) {
      return loaderScript.previousElementSibling;
    }

    if (loaderScript && loaderScript.parentElement && loaderScript.parentElement.hasAttribute('data-mbs-reviews-widget')) {
      return loaderScript.parentElement;
    }

    return document.querySelector('[data-mbs-reviews-widget]') ||
      document.getElementById('mbs-reviews-widget') ||
      null;
  }

  function loadBundle() {
    root = findRoot();
    if (!root || root.getAttribute('data-mbs-reviews-loader') === '1') return;
    root.setAttribute('data-mbs-reviews-loader', '1');
    if (!root.id) root.id = 'mbs-reviews-widget-' + Math.random().toString(36).slice(2);

    var bundle = document.createElement('script');
    bundle.src = 'https://api.barista-school.ru/widgets/reviews.bundle.js?v=20260608-2';
    bundle.async = true;
    bundle.setAttribute('data-target', '#' + root.id);
    bundle.onerror = function() {
      root.removeAttribute('data-mbs-reviews-loader');
      root.innerHTML = '<div style="padding:20px;color:#888;text-align:center;font-family:Mulish,Arial,sans-serif">Не удалось загрузить отзывы</div>';
    };
    document.head.appendChild(bundle);
  }

  if (document.readyState === 'loading') {
    setTimeout(loadBundle, 0);
  } else {
    loadBundle();
  }
})();
`;

fs.mkdirSync(outDir, { recursive: true });
fs.writeFileSync(loaderPath, loader, 'utf8');
fs.writeFileSync(bundlePath, bundled, 'utf8');
console.log('Built ' + path.relative(rootDir, loaderPath) + ' (' + Buffer.byteLength(loader) + ' bytes)');
console.log('Built ' + path.relative(rootDir, bundlePath) + ' (' + Buffer.byteLength(bundled) + ' bytes)');
